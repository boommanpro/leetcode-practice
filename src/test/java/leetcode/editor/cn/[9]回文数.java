    class Solution {

        public boolean isPalindrome(int x) {
            if (x < 0) {
                return false;
            }
            String s = Integer.toString(x);
            int l = 0;
            int r = s.length() - 1;
            while (l < r) {
                if (s.charAt(l) != s.charAt(r)) {
                    return false;
                }
                l++;
                r--;
            }
            return true;
        }
    }
