    class Solution {
        public int numFriendRequests(int[] ages) {
            int[] person = new int[121];
            for (int age : ages) {
                person[age]++;
            }
            int max = 121;
            int sum = 0;
            for (int i = 1; i < max; i++) {
                for (int j = 1; j < max; j++) {
                    if (check(i, j)) {
                        if (i == j) {
                            sum += (person[i] * (person[i] - 1));
                        } else {
                            sum += person[i] * person[j];
                        }
                    }
                }
            }
            return sum;
        }

        private boolean check(int x, int y) {
            if (y <= 0.5 * x + 7) {
                return false;
            }
            if (y > x) {
                return false;
            }
            if (y > 100 && x < 100) {
                return false;
            }
            return true;
        }
    }
