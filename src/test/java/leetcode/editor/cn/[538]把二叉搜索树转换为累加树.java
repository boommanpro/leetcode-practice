/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode(int x) { val = x; }
 * }
 */
    class Solution {

        int sum = 0;

        public TreeNode convertBST(TreeNode root) {
            sum = 0;
            dfs(root);
            return root;
        }

        private void dfs(TreeNode root) {
            if (root != null) {
                dfs(root.right);
                sum += root.val;
                root.val = sum;
                dfs(root.left);
            }
        }

    }
