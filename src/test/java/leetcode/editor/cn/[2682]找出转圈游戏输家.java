class Solution {
    public int[] circularGameLosers(int n, int k) {
        Set<Integer> set = new HashSet<>();
        int i = 1;
        int curr = 0;
        while (!set.contains(curr)) {
            set.add(curr);
            curr = (i * k + curr) % n;
            i++;
        }
        List<Integer> ans = new ArrayList<>();
        for (int j = 0; j < n; j++) {
            if (!set.contains(j)) {
                ans.add(j + 1);
            }
        }
        return ans.stream().mapToInt(Integer::intValue).toArray();
    }
}
