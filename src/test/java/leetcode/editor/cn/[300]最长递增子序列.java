    class Solution {
        public int lengthOfLIS(int[] nums) {
            int n = nums.length;
            int len = 1;
            int[] d = new int[n + 1];
            d[len] = nums[0];
            for (int i = 1; i < n; i++) {
                if (nums[i] > d[len]) {
                    d[++len] = nums[i];
                    continue;
                }
                int pos = 0;
                int l = 1;
                int r = len;
                while (l <= r) {
                    int mid = ((r - l) >> 1) + l;
                    int curr = d[mid];
                    if (nums[i] > curr) {
                        pos = mid;
                        l = mid + 1;
                    } else {
                        r = mid - 1;
                    }
                }
                d[pos + 1] = nums[i];
            }
            return len;
        }
    }
