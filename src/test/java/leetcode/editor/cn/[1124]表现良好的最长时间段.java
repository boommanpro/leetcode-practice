    class Solution {
        public int longestWPI(int[] hours) {
            Map<Integer, Integer> map = new HashMap<>();
            int s = 0;
            int ans = 0;
            for (int i = 0; i < hours.length; i++) {
                s += (hours[i] > 8 ? 1 : -1);
                if (s > 0) {
                    ans = Math.max(ans, i + 1);
                }
                if (map.containsKey(s - 1)) {
                    ans = Math.max(ans, i - map.get(s - 1));
                }
                if (!map.containsKey(s)) {
                    map.put(s, i);
                }
            }
            return ans;
        }
    }
