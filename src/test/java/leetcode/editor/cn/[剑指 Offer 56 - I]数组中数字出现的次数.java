    class Solution {

        public int[] singleNumbers(int[] nums) {
            int xor = 0;
            for (int num : nums) {
                xor ^= num;
            }
            int div = 1;
            while ((div & xor) == 0) {
                div <<= 1;
            }
            int a = 0;
            int b = 0;
            for (int num : nums) {
                if ((num & div) == 0) {
                    a ^= num;
                } else {
                    b ^= num;
                }
            }
            return new int[]{a, b};
        }
    }
