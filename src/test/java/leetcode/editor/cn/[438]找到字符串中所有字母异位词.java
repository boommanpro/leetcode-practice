    class Solution {

        public List<Integer> findAnagrams(String s, String p) {
            int[] dict = new int[26];
            int[] window = new int[26];
            List<Integer> ans = new ArrayList<>();
            for (char c : p.toCharArray()) {
                dict[c - 'a']++;
            }
            int l = 0, r = 0;
            while (r < s.length()) {
                int curr = s.charAt(r) - 'a';
                window[curr]++;
                r++;
                while (window[curr] > dict[curr]) {
                    int prev = s.charAt(l) - 'a';
                    window[prev]--;
                    l++;
                }
                if (r - l == p.length()) {
                    ans.add(l);
                }
            }
            return ans;
        }

    }
