    class Solution {
        public int[] bestCoordinate(int[][] towers, int radius) {
            int max = 50;
            int maxValue = Integer.MIN_VALUE;
            int[] ans = new int[]{0, 0};
            for (int i = 0; i <= max; i++) {
                for (int j = 0; j <= max; j++) {
                    int curr = 0;
                    for (int[] tower : towers) {
                        double distance = Math.sqrt(Math.pow(tower[0] - i, 2) + Math.pow(tower[1] - j, 2));
                        if (distance <= radius) {
                            curr += (int) Math.floor(tower[2] / (1 + distance));
                        }
                    }
                    if (curr > maxValue) {
                        ans[0] = i;
                        ans[1] = j;
                        maxValue = curr;
                    }
                }
            }
            return ans;
        }
    }
