    class Solution {

        public int maxSubArray(int[] nums) {
            int max = nums[0];
            int n = nums.length;
            for (int i = 1; i < n; i++) {
                if (nums[i - 1] > 0) {
                    nums[i] = nums[i] + nums[i - 1];
                }
                max = Math.max(max, nums[i]);
            }
            return max;
        }
    }
