    class Solution {

        public int eraseOverlapIntervals(int[][] matrix) {
            int len = matrix.length;
            if (len == 0) {
                return 0;
            }
            Arrays.sort(matrix, (o1, o2) -> {
                if (o1[1] == o2[1]) {
                    return o1[0] - o2[0];
                }
                return o1[1] - o2[1];
            });
            int[] dp = new int[len];
            Arrays.fill(dp, 1);
            for (int i = 1; i < len; i++) {
                for (int j = i - 1; j >= 0; j--) {
                    if (matrix[i][0] >= matrix[j][1]) {
                        dp[i] = Math.max(dp[i], dp[j] + 1);
                    }
                }
            }
            return len - Arrays.stream(dp).max().getAsInt();
        }
    }
